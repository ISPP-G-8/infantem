package com.isppG8.infantem.infantem.dream;

public enum DreamType {
    DEEP, // Baby moves a bit, makes some gestures and/or suckles / El bebé se mueve un poco, hace gestos o succiona
          // mientras duerme.
    LIGHT, // Baby is calm, no movements nor expressions / El bebé tiene un sueño tranquilo, sin movimientos ni
           // expresiones faciales.
    REM, // Baby's eyes moving fastly while asleep, possible movements or smiles (dreaming) / El bebé hace movimientos
         // rápidos con los ojos, posibles sonrisas o gestos.
    AGITATED // Baby moves more than usual, irregular breathing / El bebé se mueve más de lo normal, respira
             // irregularmente.

}
